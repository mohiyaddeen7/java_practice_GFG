public class demo_switch {
    public static void main(String[] args) {
        int ch = 1;


        switch (ch)
        {
            default:
                System.out.println("Default case");
                break;

            case 1:
                System.out.println("case 1");
                break;

            case 2:
                System.out.println("case 2");
                break;
        }
    }
}
